def format_word(array, emoji):
    output = """"""
    for line in array:
        for index in line:
            output += ":{e}:" if index else ":clear:"
        output += "\n"
    return output.format(e=emoji)


def get_word(word, emoji):
    output = [[], [], [], [], []]
    for letter_str in word:
        letter = letters.get(letter_str.lower())
        for index, row in enumerate(letter):
            output[index].extend(row)
    return format_word(output, emoji)


